// 辅助函数
export function getStandBySlug(slug: string): Stand | undefined {
    return stands.find((s) => s.slug === slug);
}

export function getStandsByTier(tier: Stand["tier"]): Stand[] {
    return stands.filter((s) => s.tier === tier);
}

export function getAllStandSlugs(): string[] {
    return stands.map((s) => s.slug);
}
